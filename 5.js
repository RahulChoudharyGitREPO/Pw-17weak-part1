{
  // Variable declaration using 'let'
  let letVar;

  // Attempt to log the value of 'letVar' before assignment
  console.log("Trying to access 'letVar' before assignment:", letVar);

  // Assign a value to 'letVar'
  letVar = 42;

  // Now, you can log the value
  console.log("Value of 'letVar' after assignment:", letVar);
}
